package be.thomasmore.stuvo.Models;

public class Campus {
    private long id;
    private String name;

    public Campus() {
    }

    public Campus(long id, String name) {
        this.id = id;
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
